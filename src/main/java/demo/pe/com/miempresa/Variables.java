package demo.pe.com.miempresa;

public class Variables {

	public static void main(String[] args) {
		
		// Variables

		// Variables de tipo entero (integer)
		
		int zero = 0;
		int edad = 18; 
		
		System.out.println(zero);
		System.out.println(edad);
		
		long numeroKilometros = 321321321321l;
		
		System.out.println(numeroKilometros);
		
		// Variable de tipo Decimal (float y double)
		
		float numeroPi = 3.1416f;
		
		System.out.println(numeroPi);
		
		double decimalMasLargo = 123.987;
		
		System.out.println(decimalMasLargo);

		// Variables de tipo texto
		
		String nombreJean = "Jean";
		
		System.out.println(nombreJean);
		
		char letraA = 'A';
		
		System.out.println(letraA);
		
		// Variable de estado
		
		boolean activo = true;
		boolean inactivo = false;
		
		System.out.println(activo);
		System.out.println(inactivo);
		
		System.out.println(edad);
		
		// Cambiando su valor inicial (Sobreescribir)
		
		edad = 20;
		
		System.out.println(edad);
		
		
		
		
		
	}

}
